namespace Odnoklassniki.Rest.ApiClients.PhotosV2.Datas;

/// <summary>
/// DTO для представления данных о URL загрузки фотографий в API Одноклассников (версия 2).
/// Используется для получения эндпоинта загрузки и идентификаторов фотографий перед отправкой бинарных данных.
/// </summary>
/// <remarks>
/// Класс соответствует структуре данных, возвращаемой методом API <c>photos.getUploadUrl</c> (версия 2).
/// 
/// <para><b>Полный цикл загрузки фотографии:</b></para>
/// <list type="number">
/// <item>Вызвать <c>photos.getUploadUrl</c> для получения <see cref="UploadUrlData"/>;</item>
/// <item>Загрузить изображение через <see cref="UploadUrl"/> методом POST (multipart/form-data);</item>
/// <item>Получить токены из ответа сервера загрузки;</item>
/// <item>Вызвать <c>photos.commit</c> с токенами для финализации публикации;</item>
/// <item>Получить идентификаторы опубликованных фотографий в <see cref="PhotoIds"/>.</item>
/// </list>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Подготовка к загрузке одной или нескольких фотографий в альбом;</item>
/// <item>Получение временного URL для безопасной передачи бинарных данных;</item>
/// <item>Предварительное резервирование идентификаторов для загружаемых изображений.</item>
/// </list>
/// 
/// <para><b>Важно:</b></para>
/// <list type="bullet">
/// <item>URL загрузки действителен ограниченное время (обычно несколько минут);</item>
/// <item>После истечения срока необходимо запросить новый URL;</item>
/// <item>Один URL может использоваться для загрузки нескольких фотографий в рамках одного запроса.</item>
/// </list>
/// </remarks>
public record UploadUrlData
{
    /// <summary>
    /// Коллекция идентификаторов фотографий, зарезервированных для загрузки. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>photo_ids</c> в ответе API <c>photos.getUploadUrl</c>.
    /// Содержит предварительно выделенные идентификаторы для каждой фотографии, которая будет загружена.
    /// 
    /// <para><b>Особенности:</b></para>
    /// <list type="bullet">
    /// <item>Количество ID соответствует запрошенному количеству фотографий в параметре <c>count</c>;</item>
    /// <item>Идентификаторы резервируются сервером до фактической загрузки изображений;</item>
    /// <item>При успешной загрузке и коммите эти ID становятся постоянными идентификаторами фотографий;</item>
    /// <item>При отмене загрузки ID освобождаются и могут быть использованы повторно.</item>
    /// </list>
    /// 
    /// <para><b>Пример значения:</b></para>
    /// <c>["123456", "789012", "345678"]</c> — для загрузки трёх фотографий.
    /// </remarks>
    /// <value>
    /// Коллекция строковых идентификаторов в формате OK.ru (числовые строки).
    /// Не может быть <see langword="null"/>, но может быть пустой при ошибке резервирования.
    /// </value>
    public required ICollection<string> PhotoIds { get; init; }

    /// <summary>
    /// Временный URL-адрес эндпоинта для загрузки бинарных данных фотографий. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>upload_url</c> в ответе API <c>photos.getUploadUrl</c>.
    /// Содержит прямой HTTPS-адрес на сервер загрузки изображений Одноклассников.
    /// 
    /// <para><b>Формат запроса загрузки:</b></para>
    /// <list type="bullet">
    /// <item>Метод: <c>POST</c>;</item>
    /// <item>Content-Type: <c>multipart/form-data</c>;</item>
    /// <item>Имя поля: <c>pic0</c>, <c>pic1</c>, ... (по количеству фотографий);</item>
    /// <item>Значение поля: бинарные данные изображения (Stream).</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>URL действителен ограниченное время (рекомендуется немедленная загрузка);</item>
    /// <item>Повторное использование URL после успешной загрузки невозможно;</item>
    /// <item>При ошибке загрузки необходимо запросить новый URL;</item>
    /// <item>URL содержит параметры безопасности (токен, подпись, срок действия).</item>
    /// </list>
    /// 
    /// <para><b>Пример значения:</b></para>
    /// <c>https://www.ok.ru/up?act=upload&amp;token=abc123&amp;expires=1234567890</c>
    /// </remarks>
    /// <value>
    /// Строковый URL-адрес в формате HTTPS. Не может быть пустым или <see langword="null"/>.
    /// </value>
    public required string UploadUrl { get; init; }
}