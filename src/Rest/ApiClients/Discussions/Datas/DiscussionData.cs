namespace Odnoklassniki.Rest.ApiClients.Discussions.Datas;

/// <summary>
/// Представляет данные обсуждения (дискуссии) из социальной сети «Одноклассники».
/// Обсуждение может быть связано с альбомом, группой или другим типом контента.
/// Используется для десериализации ответов API (например, методов вроде <c>discussions.get</c>)
/// и передачи данных между слоями приложения.
/// </summary>
public record DiscussionData
{
    /// <summary>
    /// Уникальный идентификатор обсуждения в системе Одноклассников.
    /// Соответствует полю <c>discussionId</c> в ответе API.
    /// </summary>
    public required string ID { get; init; }

    /// <summary>
    /// Тип обсуждения. В API Одноклассников может указывать контекст,
    /// например: "ALBUM" (альбом), "GROUP" (группа), "PHOTO", "VIDEO" и т.д.
    /// Значение зависит от источника создания обсуждения.
    /// </summary>
    public required string Type { get; init; }

    /// <summary>
    /// Количество новых (непрочитанных) комментариев в обсуждении для текущего пользователя.
    /// Используется для отображения уведомлений или индикаторов активности.
    /// </summary>
    public required int NewCommentCount { get; init; }

    /// <summary>
    /// Идентификатор группы (сообщества), к которой относится обсуждение.
    /// Может быть <see langword="null"/>, если обсуждение не связано с группой
    /// (например, личный альбом пользователя).
    /// В API Одноклассников соответствует <c>groupId</c> или <c>clubId</c>.
    /// </summary>
    public string? GroupId { get; init; }

    /// <summary>
    /// Идентификатор альбома, с которым связано обсуждение.
    /// В Одноклассниках обсуждения часто привязаны к альбомам фотографий.
    /// Обязательное поле, даже если обсуждение не относится к фото — в таком случае
    /// может содержать технический ID или идентификатор другого типа медиаконтента.
    /// </summary>
    public required string AlbumId { get; init; }

    /// <summary>
    /// Заголовок (название) обсуждения. Может совпадать с названием альбома,
    /// темой поста или быть сгенерирован автоматически.
    /// Отображается в интерфейсе приложения.
    /// </summary>
    public required string Title { get; init; }
}