namespace Odnoklassniki.Rest.ApiClients.Photos.Datas;

/// <summary>
/// DTO для представления информации о фотографии в Одноклассниках.
/// Используется для передачи данных фотографий при получении списков и управлении медиа-контентом.
/// </summary>
/// <remarks>
/// Класс соответствует структуре данных, возвращаемой методами API работы с фотографиями
/// (например, <c>photos.get</c>, <c>photos.getAlbums</c> с параметром <c>fields=photos</c>).
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Отображение списка фотографий в альбоме или на странице пользователя;</item>
/// <item>Получение URL изображения максимального размера для отображения;</item>
/// <item>Идентификация фотографии при операциях редактирования или удаления.</item>
/// </list>
/// </remarks>
public record PhotoData
{
    /// <summary>
    /// Текстовое описание (подпись) фотографии. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>text</c> или <c>description</c> в ответе API Одноклассников.
    /// Может содержать произвольный текст, включая пробелы и специальные символы.
    /// Если фотография без описания, возвращается пустая строка.
    /// 
    /// <para><b>Примеры значений:</b></para>
    /// <list type="bullet">
    /// <item>«Отпуск в Сочи, 2024»;</item>
    /// <item>Пустая строка — если описание не указано;</item>
    /// <item>«Профильное фото» — для аватаров.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение описания фотографии. Не может быть <see langword="null"/>,
    /// но может быть пустой строкой, если описание отсутствует.
    /// </value>
    public required string Text { get; init; }

    /// <summary>
    /// Уникальный идентификатор фотографии в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>id</c> или <c>pid</c> в ответе API.
    /// Значение формируется сервером OK.ru при загрузке фотографии и не изменяется.
    /// Используется как ключевой параметр для всех операций с фотографией:
    /// <list type="bullet">
    /// <item>Получение информации о фотографии (<c>photos.getInfo</c>);</item>
    /// <item>Редактирование описания или тегов;</item>
    /// <item>Удаление фотографии (<c>photos.remove</c>);</item>
    /// <item>Добавление в альбом или изменение порядка.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/>.
    /// </value>
    public required string Id { get; init; }

    /// <summary>
    /// URL-адрес изображения максимального доступного размера. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>pic_max</c> или <c>max_src</c> в ответе API.
    /// Содержит прямой ссылку на файл изображения на CDN серверах Одноклассников.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>URL действителен ограниченное время (зависит от настроек CDN OK.ru);</item>
    /// <item>Для отображения в интерфейсе рекомендуется кэшировать изображение локально;</item>
    /// <item>Прямой доступ к URL может быть заблокирован при превышении лимитов запросов;</item>
    /// <item>Размер изображения определяется настройками загрузки и оригинальным файлом.</item>
    /// </list>
    /// 
    /// <para><b>Пример значения:</b></para>
    /// <c>https://i.mycdn.me/getPhoto?id=123&ts=456&sig=abc</c>
    /// </remarks>
    /// <value>
    /// Строковый URL-адрес изображения в формате HTTPS.
    /// Не может быть пустым или <see langword="null"/> для существующих фотографий.
    /// </value>
    public required string PicMax { get; init; }

    /// <summary>
    /// Идентификатор пользователя, загрузившего фотографию. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>uid</c> или <c>owner_id</c> в ответе API.
    /// Значение идентифицирует владельца фотографии в системе Одноклассников.
    /// 
    /// <para><b>Примечания:</b></para>
    /// <list type="bullet">
    /// <item>Для фотографий из групп может содержать ID группы или ID загрузившего пользователя;</item>
    /// <item>Используется для проверки прав доступа к фотографии;</item>
    /// <item>При работе с чужими фотографиями может потребоваться дополнительная проверка приватности.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор пользователя в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/>.
    /// </value>
    public required string UserId { get; init; }
}