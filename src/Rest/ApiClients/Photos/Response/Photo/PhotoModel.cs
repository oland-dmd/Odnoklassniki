using System.Text.Json.Serialization;

namespace Odnoklassniki.Rest.ApiClients.Photos.Response.Photo;

/// <summary>
/// Внутренняя модель ответа API Одноклассников при получении информации о фотографии.
/// Используется для десериализации JSON-ответа от методов работы с фотографиями
/// (например, <c>photos.get</c>, <c>photos.getUserPhotos</c>).
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="Datas.PhotoData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "text": "Отпуск в Сочи",
///   "id": "123456",
///   "pic_max": "https://i.mycdn.me/getPhoto?id=123",
///   "user_id": "789012"
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Получение списка фотографий в альбоме или на странице пользователя;</item>
/// <item>Извлечение URL изображения максимального размера для отображения;</item>
/// <item>Преобразование в публичную модель <see cref="Datas.PhotoData"/> для клиентского кода.</item>
/// </list>
/// 
/// <para><b>Особенности полей:</b></para>
/// Некоторые поля могут отсутствовать в ответе (nullable) в зависимости от:
/// <list type="bullet">
/// <item>Параметров запроса (не все поля запрашиваются по умолчанию);</item>
/// <item>Настроек приватности фотографии;</item>
/// <item>Типа фотографии (аватар, фото из альбома, временное фото).</item>
/// </list>
/// </remarks>
internal class PhotoModel
{
    /// <summary>
    /// Текстовое описание (подпись) фотографии.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>text</c> в ответе API Одноклассников.
    /// Может содержать произвольный текст, включая пробелы и специальные символы.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле nullable — может отсутствовать, если описание не задано;</item>
    /// <item>Пустая строка или <see langword="null"/> означают отсутствие описания;</item>
    /// <item>Максимальная длина текста ограничена настройками платформы OK.ru.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение описания фотографии.
    /// Может быть <see langword="null"/> или пустой строкой, если описание отсутствует.
    /// </value>
    [JsonPropertyName("text")]
    public string? Text { get; init; }

    /// <summary>
    /// Уникальный идентификатор фотографии в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>id</c> или <c>pid</c> в ответе API.
    /// Значение формируется сервером OK.ru при загрузке фотографии и не изменяется.
    /// Используется как ключевой параметр для всех операций с фотографией:
    /// <list type="bullet">
    /// <item>Получение информации о фотографии (<c>photos.getInfo</c>);</item>
    /// <item>Редактирование описания или тегов;</item>
    /// <item>Удаление фотографии (<c>photos.remove</c>);</item>
    /// <item>Добавление в альбом или изменение порядка.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/>.
    /// </value>
    [JsonPropertyName("id")]
    public required string Id { get; init; }

    /// <summary>
    /// URL-адрес изображения максимального доступного размера.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>pic_max</c> в ответе API.
    /// Содержит прямую ссылку на файл изображения на CDN серверах Одноклассников.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле nullable — может отсутствовать для удалённых или недоступных фотографий;</item>
    /// <item>URL действителен ограниченное время (зависит от настроек CDN OK.ru);</item>
    /// <item>Для отображения в интерфейсе рекомендуется кэшировать изображение локально;</item>
    /// <item>Прямой доступ к URL может быть заблокирован при превышении лимитов запросов.</item>
    /// </list>
    /// 
    /// <para><b>Пример значения:</b></para>
    /// <c>https://i.mycdn.me/getPhoto?id=123&amp;ts=456&amp;sig=abc</c>
    /// </remarks>
    /// <value>
    /// Строковый URL-адрес изображения в формате HTTPS.
    /// Может быть <see langword="null"/>, если изображение недоступно или не запрошено.
    /// </value>
    [JsonPropertyName("pic_max")]
    public string? PicMax { get; init; }

    /// <summary>
    /// Идентификатор пользователя, загрузившего фотографию.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>user_id</c> или <c>uid</c> в ответе API.
    /// Значение идентифицирует владельца фотографии в системе Одноклассников.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле nullable — может отсутствовать для анонимных или системных фотографий;</item>
    /// <item>Для фотографий из групп может содержать ID группы или ID загрузившего пользователя;</item>
    /// <item>Используется для проверки прав доступа к фотографии;</item>
    /// <item>При работе с чужими фотографиями может потребоваться дополнительная проверка приватности.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор пользователя в формате OK.ru (числовая строка).
    /// Может быть <see langword="null"/>, если владелец не определён или не запрошен.
    /// </value>
    [JsonPropertyName("user_id")]
    public string? UserId { get; init; }
}