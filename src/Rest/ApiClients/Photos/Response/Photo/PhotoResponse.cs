using System.Text.Json.Serialization;

namespace Odnoklassniki.Rest.ApiClients.Photos.Response.Photo;

/// <summary>
/// Внутренняя модель обёртки ответа API Одноклассников при операциях с единичной фотографией.
/// Используется для десериализации JSON-ответа от методов получения информации о фотографии.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="Datas.PhotoData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "photo": {
///     "text": "Отпуск в Сочи",
///     "id": "123456",
///     "pic_max": "https://i.mycdn.me/getPhoto?id=123",
///     "user_id": "789012"
///   }
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Обработка ответа от метода <c>photos.getInfo</c> при получении деталей фотографии;</item>
/// <item>Обработка ответа от метода <c>photos.upload</c> после загрузки нового изображения;</item>
/// <item>Преобразование в публичную модель <see cref="Datas.PhotoData"/> для передачи в клиентский код.</item>
/// </list>
/// 
/// <para><b>Важно:</b></para>
/// Структура ответа с обёрткой <c>photo</c> используется для единичных операций с фотографией.
/// Для списков фотографий может возвращаться массив напрямую без обёртки.
/// </remarks>
internal class PhotoResponse
{
    /// <summary>
    /// Объект с информацией о фотографии. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>photo</c> в ответе API Одноклассников.
    /// Содержит десериализованные данные фотографии в модели <see cref="PhotoModel"/>.
    /// 
    /// <para><b>Состав данных:</b></para>
    /// <list type="bullet">
    /// <item><see cref="PhotoModel.Id"/> — уникальный идентификатор фотографии;</item>
    /// <item><see cref="PhotoModel.Text"/> — текстовое описание (подпись);</item>
    /// <item><see cref="PhotoModel.PicMax"/> — URL изображения максимального размера;</item>
    /// <item><see cref="PhotoModel.UserId"/> — идентификатор владельца фотографии.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле обязательное — при успешной операции всегда содержит валидный объект;</item>
    /// <item>Используется для извлечения ID загруженной фотографии при последующих операциях;</item>
    /// <item>При ошибке загрузки или получения информации ответ может содержать ошибку вместо фотографии.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Экземпляр <see cref="PhotoModel"/> с данными фотографии.
    /// Не может быть <see langword="null"/> для успешного ответа.
    /// </value>
    [JsonPropertyName("photo")]
    public required PhotoModel Photo { get; init; }
}