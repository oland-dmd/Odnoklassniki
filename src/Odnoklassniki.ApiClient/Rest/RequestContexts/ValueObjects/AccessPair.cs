namespace Odnoklassniki.Rest.RequestContexts.ValueObjects;

/// <summary>
/// Парная структура учётных данных для авторизации запросов к API Одноклассников (OK.ru).
/// Содержит связку «токен доступа — секрет сессии», необходимую для расчёта криптографической подписи (<c>sig</c>)
/// и выполнения методов от имени пользователя. Используется внутри <see cref="IRequestContext"/>.
/// </summary>
/// <remarks>
/// Структура является неизменяемой (immutable) и передаётся по значению.
/// Предназначена только для внутреннего использования в клиенте API — не сохраняйте экземпляры
/// в логах, кэше или передавайте во внешние системы.
/// <list type="bullet">
/// <item><description>Секрет сессии (<c>SessionSecretKey</c>) является конфиденциальными данными — обработка должна соответствовать политикам безопасности.</description></item>
/// <item><description>При использовании OAuth-потока значения извлекаются из ответа метода авторизации <c>oauth.authorize</c>.</description></item>
/// <item><description>Для режима приложения (без пользователя) поле <c>SessionSecretKey</c> может быть пустым — подпись рассчитывается через <c>application_secret</c>.</description></item>
/// </list>
/// </remarks>
public readonly record struct AccessPair
{
    /// <summary>
    /// OAuth-токен доступа пользователя, полученный после успешной авторизации.
    /// </summary>
    /// <remarks>
    /// Обязательное поле для методов, требующих авторизации от имени пользователя.
    /// Имеет ограниченный срок жизни (зависит от прав доступа: <c>LONG_ACCESS_TOKEN</c> — до 30 дней).
    /// При истечении токена необходимо выполнить повторную авторизацию или использовать механизм продления сессии.
    /// Формат: строка, выданная сервером авторизации OK.ru.
    /// </remarks>
    public string AccessToken { get; init; }

    /// <summary>
    /// Секретный ключ сессии (<c>session_secret_key</c>), используемый для расчёта подписи запросов.
    /// </summary>
    /// <remarks>
    /// Обязательное поле при работе в режиме пользовательской сессии.
    /// Генерируется сервером OK.ru при авторизации и действителен в течение времени жизни сессии.
    /// <list type="bullet">
    /// <item><description>Не передавайте значение в логах, ответах клиенту или системах мониторинга.</description></item>
    /// <item><description>При компрометации ключа необходимо немедленно отозвать сессию через метод <c>auth.logout</c>.</description></item>
    /// <item><description>Для режима приложения может быть пустой строкой — в этом случае подпись рассчитывается через <c>application_secret</c>.</description></item>
    /// </list>
    /// </remarks>
    public string SessionSecretKey { get; init; }
}