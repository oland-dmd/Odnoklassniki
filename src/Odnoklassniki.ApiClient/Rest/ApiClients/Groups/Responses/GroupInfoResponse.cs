using System.Text.Json.Serialization;
using Odnoklassniki.Rest.ApiClients.Groups.Dtos;

namespace Odnoklassniki.Rest.ApiClients.Groups.Responses;

/// <summary>
/// Внутренняя модель ответа API Одноклассников при получении информации о группе.
/// Используется для десериализации JSON-ответа от метода <c>groups.getInfo</c>.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="GroupInfoDto"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "name": "Название группы",
///   "uid": "123456",
///   "attrs": { ... }
/// }
/// </code>
/// </remarks>
internal record GroupInfoResponse
{
    /// <summary>
    /// Отображаемое название группы в системе Одноклассников.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>name</c> в ответе API.
    /// Может содержать произвольный текст, включая пробелы и специальные символы.
    /// Максимальная длина ограничена настройками платформы OK.ru.
    /// </remarks>
    /// <value>
    /// Строковое значение названия группы. Не может быть пустым для существующих групп.
    /// </value>
    [JsonPropertyName("name")]
    public string Name { get; init; }

    /// <summary>
    /// Уникальный идентификатор группы в системе Одноклассников.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>uid</c> в ответе API (внутреннее название поля в OK.ru).
    /// Значение формируется сервером при создании группы и не изменяется.
    /// Используется как ключевой параметр для всех операций с группой.
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// </value>
    [JsonPropertyName("uid")]
    public string Id { get; init; }

    /// <summary>
    /// Дополнительные атрибуты группы (настройки, метаданные, флаги).
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>attrs</c> в ответе API.
    /// Содержит расширенную информацию о группе: тип, статус, настройки приватности,
    /// количество участников и другие служебные данные.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле может отсутствовать в ответе, если не запрошено явно в параметрах метода;</item>
    /// <item>Структура атрибутов определяется версией API и типом запроса;</item>
    /// <item>Десериализуется в модель <see cref="OkAttributes"/>.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Объект <see cref="OkAttributes"/> с дополнительными данными группы.
    /// Может быть <see langword="null"/>, если атрибуты не возвращены сервером.
    /// </value>
    [JsonPropertyName("attrs")]
    public Models.Attributes? Attributes { get; init; }
}