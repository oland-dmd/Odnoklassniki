namespace Odnoklassniki.Rest.ApiClients.Groups.Dtos;

/// <summary>
/// Статус участия пользователя в группе Одноклассников.
/// Определяет роль и уровень доступа пользователя в контексте конкретной группы.
/// </summary>
/// <remarks>
/// Значения перечисления соответствуют статусам, возвращаемым методами API Одноклассников
/// (например, <c>groups.getMembers</c>, <c>users.getInfo</c> с параметром <c>fields=group_status</c>).
/// Используется для фильтрации участников, проверки прав доступа и отображения информации в интерфейсе.
/// </remarks>
public enum GroupStatus
{
    /// <summary>
    /// Активный участник группы. Имеет стандартные права на просмотр контента и участие в обсуждениях.
    /// </summary>
    ACTIVE,

    /// <summary>
    /// Пользователь находится в статусе «возможно» (подтвердил участие, но с ограниченной активностью).
    /// Используется для мероприятий и встреч, где участник может указать статус «пойду»/«возможно».
    /// </summary>
    MAYBE,

    /// <summary>
    /// Пользователь заблокирован в группе. Не имеет доступа к контенту и не может взаимодействовать с группой.
    /// </summary>
    /// <remarks>
    /// Блокировка может быть наложена администратором или модератором группы.
    /// Запросы от такого пользователя к методам группы будут отклонены с ошибкой доступа.
    /// </remarks>
    BLOCKED,

    /// <summary>
    /// Администратор группы. Обладает полными правами управления: редактирование настроек, модерация, управление участниками.
    /// </summary>
    /// <remarks>
    /// Наивысший уровень доступа. Может назначать модераторов, изменять тип группы, удалять контент.
    /// </remarks>
    ADMIN,

    /// <summary>
    /// Модератор группы. Имеет права на модерацию контента и участников, но не может изменять настройки группы.
    /// </summary>
    /// <remarks>
    /// Права назначаются администратором. Включают: удаление постов, бан пользователей, одобрение заявок на вступление.
    /// </remarks>
    MODERATOR,

    /// <summary>
    /// Пассивный участник. Пользователь состоит в группе, но не проявляет активности (не просматривал контент за длительный период).
    /// </summary>
    /// <remarks>
    /// Статус используется для аналитики и сегментации аудитории. Не влияет на базовые права доступа.
    /// </remarks>
    PASSIVE,

    /// <summary>
    /// Статус не определён или не передан в ответе API.
    /// </summary>
    /// <remarks>
    /// Может возвращаться, если пользователь не является участником группы, либо поле <c>group_status</c> не было запрошено.
    /// Также используется как значение по умолчанию при десериализации неизвестных статусов.
    /// </remarks>
    UNKNOWN
}