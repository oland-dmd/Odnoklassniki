namespace Odnoklassniki.Enums;

/// <summary>
/// Направление навигации при постраничной загрузке данных (пагинации).
/// Определяет относительное положение загружаемых данных по отношению к текущему анкору.
/// </summary>
public enum PagingDirection
{
    /// <summary>
    /// Загрузка данных вокруг текущей позиции (анкора).
    /// Возвращает записи как старше, так и моложе указанной точки выборки.
    /// </summary>
    AROUND,

    /// <summary>
    /// Загрузка следующих данных (вперёд по хронологии или порядку следования).
    /// Используется для перехода к следующей странице результата.
    /// </summary>
    FORWARD,

    /// <summary>
    /// Загрузка предыдущих данных (назад по хронологии или порядку следования).
    /// Используется для перехода к предыдущей странице результата.
    /// </summary>
    BACKWARD
}