using System.Text.Json.Serialization;

#pragma warning disable CS8618

namespace Oland.Odnoklassniki.Image;

/// <summary>
/// DTO для представления токена загруженного изображения в API Одноклассников.
/// Используется при загрузке фотографий через метод <c>photos.getUploadUrl</c> и последующем коммите.
/// </summary>
/// <remarks>
/// Токен является временным идентификатором загруженного изображения на серверах OK.ru.
/// После успешной загрузки изображения токены передаются в метод <c>photos.commit</c> для финализации публикации.
/// Срок жизни токена ограничен (обычно несколько часов), после чего он становится недействительным.
/// </remarks>
public record TokenInfo
{
    /// <summary>
    /// Уникальный токен загруженного изображения, выданный сервером Одноклассников.
    /// Обязательное поле для последующей публикации фотографии.
    /// </summary>
    /// <remarks>
    /// Формат токена определяется внутренней реализацией OK.ru (обычно строка Base64 или UUID).
    /// Используется в методе <c>photos.commit</c> в параметре <c>photos</c> в формате: <c>pic0:token1,pic1:token2</c>.
    /// Токен действителен только для текущего сеанса загрузки и не может быть использован повторно.
    /// </remarks>
    /// <value>
    /// Строковое значение токена. Не может быть пустым или <see langword="null"/> после успешной загрузки.
    /// </value>
    [JsonPropertyName("token")]
    public string Token { get; set; }
}