using Oland.Odnoklassniki.Enums;

namespace Oland.Odnoklassniki.Rest.AnchorNavigators;

/// <summary>
/// Конфигурация пагинации для навигаторов на основе якорей (anchor-based pagination).
/// Используется в методах, возвращающих <see cref="AnchorNavigator{T}"/>, для управления направлением,
/// размером страницы и точкой продолжения итерации по коллекции.
/// </summary>
public record struct AnchorConfiguration()
{
    /// <summary>
    /// Направление обхода коллекции при пагинации.
    /// </summary>
    /// <remarks>
    /// По умолчанию установлено значение <see cref="PagingDirection.FORWARD"/> (прямой порядок).
    /// Значение <see cref="PagingDirection.BACKWARD"/> используется для навигации в обратном направлении
    /// (например, при загрузке предыдущей страницы в бесконечном скролле).
    /// </remarks>
    public PagingDirection Direction { get; init; } = PagingDirection.FORWARD;

    /// <summary>
    /// Максимальное количество элементов, возвращаемых на одной странице.
    /// </summary>
    /// <remarks>
    /// По умолчанию: <c>100</c>. Допустимый диапазон определяется ограничениями внешнего API Одноклассников.
    /// При указании значения, превышающего серверный лимит, запрос может быть отклонён или автоматически
    /// скорректирован на стороне платформы. Рекомендуется не превышать значение 1000 для стабильной работы.
    /// </remarks>
    public int Count { get; init; } = 100;
    
    /// <summary>
    /// Якорь (cursor) для продолжения итерации с определённой позиции коллекции.
    /// </summary>
    /// <remarks>
    /// По умолчанию: пустая строка (начало коллекции). Значение якоря формируется сервером ОК.ру
    /// и передаётся в ответе навигатора. Не следует модифицировать или генерировать якорь вручную —
    /// используйте значение, полученное из предыдущего ответа <see cref="AnchorNavigator{T}"/>.
    /// Пустое значение интерпретируется как запрос первой страницы.
    /// </remarks>
    public string Anchor { get; init; } = string.Empty;
}