namespace Oland.Odnoklassniki.Rest.ApiClients.Market.Models;

/// <summary>
/// Модель представления фотографии товара в маркетплейсе Одноклассников.
/// </summary>
/// <remarks>
/// Используется как элемент коллекции <see cref="ProductModel.Photos"/>.
/// Фотография идентифицируется токеном, полученным после предварительной загрузки
/// через медиасервис Одноклассников (без вызова метода <c>commit</c>).
/// </remarks>
public record PhotoModel
{
    /// <summary>
    /// Уникальный токен фотографии, полученный после загрузки в медиасервис.
    /// Обязательное поле. Используется для ссылки на изображение при создании/редактировании товара.
    /// </summary>
    /// <remarks>
    /// Токен формируется сервером Одноклассников при загрузке файла через API медиа.
    /// Пример формата: строка из латинских букв, цифр и символов (например, «1234567890_abcDEF»).
    /// </remarks>
    public required string PhotoToken { get; init; }
    
    /// <summary>
    /// Проверяет валидность модели фотографии.
    /// </summary>
    /// <value>
    /// <c>true</c>, если <see cref="PhotoToken"/> не пуст и не состоит только из пробельных символов;
    /// иначе <c>false</c>.
    /// </value>
    public bool IsValid => !string.IsNullOrWhiteSpace(PhotoToken);
}