namespace Oland.Odnoklassniki.Rest.ApiClients.PhotosV2.Datas;

/// <summary>
/// DTO для представления результата коммита (финализации) загруженной фотографии в API Одноклассников.
/// Используется после загрузки изображения через <c>photos.getUploadUrl</c> и <c>photos.upload</c>.
/// </summary>
/// <remarks>
/// Класс соответствует структуре данных, возвращаемой методом API <c>photos.commit</c> (версия 2).
/// Коммит — обязательный этап загрузки фотографии: без него изображение остаётся временным и не публикуется.
/// 
/// <para><b>Полный цикл загрузки фотографии:</b></para>
/// <list type="number">
/// <item>Получить URL загрузки через <c>photos.getUploadUrl</c>;</item>
/// <item>Загрузить изображение через <c>photos.upload</c> (возвращает токены);</item>
/// <item>Вызвать <c>photos.commit</c> с токенами для финализации;</item>
/// <item>Получить <see cref="CommitPhotoData"/> с ID и статусом опубликованной фотографии.</item>
/// </list>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Подтверждение успешной публикации фотографии;</item>
/// <item>Получение идентификатора фотографии для последующих операций;</item>
/// <item>Проверка статуса коммита для обработки ошибок загрузки.</item>
/// </list>
/// </remarks>
public record CommitPhotoData
{
    /// <summary>
    /// Статус выполнения операции коммита фотографии. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>status</c> в ответе API <c>photos.commit</c>.
    /// Определяет результат финализации загрузки изображения.
    /// 
    /// <para><b>Возможные значения:</b></para>
    /// <list type="bullet">
    /// <item><c>"OK"</c> — фотография успешно опубликована;</item>
    /// <item><c>"ERROR"</c> — ошибка коммита (неверный токен, истёк срок жизни);</item>
    /// <item><c>"PENDING"</c> — фотография на модерации (для некоторых типов контента);</item>
    /// <item><c>"REJECTED"</c> — фотография отклонена модерацией.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Только статус <c>"OK"</c> гарантирует доступность фотографии для просмотра;</item>
    /// <item>При других статусах рекомендуется повторить коммит или проверить токен;</item>
    /// <item>Список статусов может расширяться в новых версиях API.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение статуса операции. Не может быть пустым или <see langword="null"/>.
    /// </value>
    public required string Status { get; init; }

    /// <summary>
    /// Уникальный идентификатор опубликованной фотографии в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>id</c> или <c>pid</c> в ответе API <c>photos.commit</c>.
    /// Значение формируется сервером OK.ru после успешной финализации загрузки.
    /// 
    /// <para><b>Использование идентификатора:</b></para>
    /// <list type="bullet">
    /// <item>Получение информации о фотографии (<c>photos.getInfo</c>);</item>
    /// <item>Редактирование описания, тегов, альбомной принадлежности;</item>
    /// <item>Удаление фотографии (<c>photos.remove</c>);</item>
    /// <item>Добавление в посты, комментарии, сообщения;</item>
    /// <item>Формирование прямых ссылок на изображение.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>ID действителен только после успешного коммита (статус <c>"OK"</c>);</item>
    /// <item>Идентификатор неизменяем и уникален в пределах всей платформы;</item>
    /// <item>Формат ID — строковое представление числового значения (например, <c>"123456789"</c>).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/> для успешного коммита.
    /// </value>
    public required string Id { get; init; }
}