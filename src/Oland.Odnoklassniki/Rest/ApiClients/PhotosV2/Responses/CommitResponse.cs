using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.PhotosV2.Responses;

/// <summary>
/// Внутренняя модель ответа API Одноклассников (версия 2) при финализации (коммите) загруженных фотографий.
/// Используется для десериализации JSON-ответа от метода <c>photos.commit</c>.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, коллекцию <see cref="Datas.CommitPhotoData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "photos": [
///     { "status": "OK", "assigned_photo_id": "123456789" },
///     { "status": "OK", "assigned_photo_id": "987654321" }
///   ]
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Обработка ответа от метода <c>photos.commit</c> после загрузки одного или нескольких изображений;</item>
/// <item>Проверка статуса публикации каждой фотографии в пакетной загрузке;</item>
/// <item>Извлечение идентификаторов опубликованных фотографий для последующих операций;</item>
/// <item>Преобразование массива <see cref="Photos"/> в публичные DTO для клиентского кода.</item>
/// </list>
/// 
/// <para><b>Особенности пакетной загрузки:</b></para>
/// <list type="bullet">
/// <item>Массив <see cref="Photos"/> может содержать несколько фотографий при одновременной загрузке;</item>
/// <item>Каждая фотография имеет независимый статус коммита (может частично завершиться ошибкой);</item>
/// <item>Порядок элементов в массиве соответствует порядку загруженных изображений;</item>
/// <item>Рекомендуется проверять статус каждой фотографии перед использованием идентификаторов.</item>
/// </list>
/// 
/// <para><b>Важно:</b></para>
/// Модель используется только в API версии 2 (PhotosV2). Для legacy-версии см. <c>Photos.Response</c>.
/// </remarks>
public record CommitResponse
{
    /// <summary>
    /// Массив результатов коммита для каждой загруженной фотографии. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>photos</c> в ответе API <c>photos.commit</c>.
    /// Содержит коллекцию объектов <see cref="CommitPhotoModel"/> со статусами и идентификаторами.
    /// 
    /// <para><b>Состав данных для каждой фотографии:</b></para>
    /// <list type="bullet">
    /// <item><see cref="CommitPhotoModel.Status"/> — статус публикации (OK, ERROR, PENDING, REJECTED);</item>
    /// <item><see cref="CommitPhotoModel.Id"/> — уникальный идентификатор опубликованной фотографии.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Массив может быть пустым при ошибке загрузки или отсутствии фотографий;</item>
    /// <item>Количество элементов соответствует числу загруженных изображений в запросе;</item>
    /// <item>Рекомендуется фильтровать фотографии по статусу <c>"OK"</c> перед использованием;</item>
    /// <item>При частичной ошибке некоторые фотографии могут быть опубликованы, другие — нет.</item>
    /// </list>
    /// 
    /// <para><b>Пример обработки:</b></para>
    /// <code>
    /// var publishedIds = response.Photos
    ///     .Where(p => p.Status == "OK")
    ///     .Select(p => p.Id)
    ///     .ToList();
    /// </code>
    /// </remarks>
    /// <value>
    /// Массив объектов <see cref="CommitPhotoModel"/>. Не может быть <see langword="null"/>,
    /// но может быть пустым при отсутствии успешно обработанных фотографий.
    /// </value>
    [JsonPropertyName("photos")]
    public required CommitPhotoModel[] Photos { get; init; }
}