using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.PhotosV2.Responses;

/// <summary>
/// Внутренняя модель ответа API Одноклассников (версия 2) при финализации (коммите) загруженной фотографии.
/// Используется для десериализации JSON-ответа от метода <c>photos.commit</c>.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="Datas.CommitPhotoData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "status": "OK",
///   "assigned_photo_id": "123456789"
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Обработка ответа от метода <c>photos.commit</c> после загрузки изображения;</item>
/// <item>Проверка статуса публикации фотографии;</item>
/// <item>Извлечение идентификатора опубликованной фотографии для последующих операций;</item>
/// <item>Преобразование в публичную модель <see cref="Datas.CommitPhotoData"/> для клиентского кода.</item>
/// </list>
/// 
/// <para><b>Важно:</b></para>
/// Модель используется только в API версии 2 (PhotosV2). Для legacy-версии см. <c>Photos.Response</c>.
/// </remarks>
public record CommitPhotoModel
{
    /// <summary>
    /// Статус выполнения операции коммита фотографии.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>status</c> в ответе API <c>photos.commit</c>.
    /// Определяет результат финализации загрузки изображения.
    /// 
    /// <para><b>Возможные значения:</b></para>
    /// <list type="bullet">
    /// <item><c>"OK"</c> — фотография успешно опубликована и доступна для просмотра;</item>
    /// <item><c>"ERROR"</c> — ошибка коммита (неверный токен, истёк срок жизни токена);</item>
    /// <item><c>"PENDING"</c> — фотография на модерации (требуется время на проверку);</item>
    /// <item><c>"REJECTED"</c> — фотография отклонена модерацией (нарушение правил).</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Только статус <c>"OK"</c> гарантирует полную доступность фотографии;</item>
    /// <item>При статусе <c>"PENDING"</c> фотография может быть недоступна до завершения модерации;</item>
    /// <item>При статусе <c>"ERROR"</c> или <c>"REJECTED"</c> рекомендуется повторить загрузку;</item>
    /// <item>Список статусов может расширяться в новых версиях API.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение статуса операции.
    /// Может быть пустой строкой при ошибке десериализации, но не должен быть <see langword="null"/>.
    /// </value>
    [JsonPropertyName("status")]
    public string Status { get; init; }

    /// <summary>
    /// Уникальный идентификатор опубликованной фотографии, назначенный сервером Одноклассников.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>assigned_photo_id</c> в ответе API <c>photos.commit</c>.
    /// Значение формируется сервером OK.ru после успешной финализации загрузки.
    /// 
    /// <para><b>Использование идентификатора:</b></para>
    /// <list type="bullet">
    /// <item>Получение информации о фотографии (<c>photos.getInfo</c>);</item>
    /// <item>Редактирование описания, тегов, альбомной принадлежности;</item>
    /// <item>Удаление фотографии (<c>photos.remove</c>);</item>
    /// <item>Добавление в посты, комментарии, сообщения;</item>
    /// <item>Формирование прямых ссылок на изображение.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>ID действителен только после успешного коммита (статус <c>"OK"</c>);</item>
    /// <item>Идентификатор неизменяем и уникален в пределах всей платформы;</item>
    /// <item>Формат ID — строковое представление числового значения (например, <c>"123456789"</c>);</item>
    /// <item>При ошибке коммита поле может отсутствовать или содержать невалидное значение.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Может быть пустой строкой или <see langword="null"/> при неудачном коммите.
    /// </value>
    [JsonPropertyName("assigned_photo_id")]
    public string Id { get; init; }
}