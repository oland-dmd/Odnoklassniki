using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.Users.Responses;

/// <summary>
/// Внутренняя модель ответа API Одноклассников при получении информации о пользователе.
/// Используется для десериализации JSON-ответа от методов работы с пользователями
/// (например, <c>users.getInfo</c>, <c>users.getCurrentUser</c>).
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="Datas.UserData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "first_name": "Иван",
///   "last_name": "Иванов",
///   "uid": "123456789"
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Получение информации о текущем авторизованном пользователе;</item>
/// <item>Получение данных о других пользователях по их идентификаторам;</item>
/// <item>Преобразование в публичную модель <see cref="Datas.UserData"/> для клиентского кода.</item>
/// </list>
/// 
/// <para><b>Важно:</b></para>
/// <list type="bullet">
/// <item>Это базовая модель — расширенная информация запрашивается через параметр <c>fields</c>;</item>
/// <item>Доступ к данным пользователя может быть ограничен настройками приватности;</item>
/// <item>Для массового получения данных о нескольких пользователях используется массив этой модели.</item>
/// </list>
/// </remarks>
internal record UserInfoResponse
{
    /// <summary>
    /// Имя пользователя в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>first_name</c> в ответе API Одноклассников.
    /// Содержит реальное или указанное пользователем имя.
    /// 
    /// <para><b>Особенности:</b></para>
    /// <list type="bullet">
    /// <item>Может содержать кириллические или латинские символы;</item>
    /// <item>Регистр символов сохраняется как в профиле пользователя;</item>
    /// <item>Для анонимных или удалённых аккаунтов может содержать служебное значение;</item>
    /// <item>Максимальная длина ограничена настройками платформы OK.ru (обычно до 50 символов).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение имени пользователя. Не может быть пустым или <see langword="null"/>.
    /// </value>
    [JsonPropertyName("first_name")]
    public required string FirstName { get; init; }

    /// <summary>
    /// Фамилия пользователя в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>last_name</c> в ответе API Одноклассников.
    /// Содержит реальную или указанную пользователем фамилию.
    /// 
    /// <para><b>Особенности:</b></para>
    /// <list type="bullet">
    /// <item>Может содержать кириллические или латинские символы;</item>
    /// <item>Регистр символов сохраняется как в профиле пользователя;</item>
    /// <item>Для некоторых пользователей может быть пустой (если не указана в профиле);</item>
    /// <item>Максимальная длина ограничена настройками платформы OK.ru (обычно до 50 символов).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение фамилии пользователя. Не может быть пустым или <see langword="null"/>.
    /// </value>
    [JsonPropertyName("last_name")]
    public required string LastName { get; init; }

    /// <summary>
    /// Уникальный идентификатор пользователя в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>uid</c> в ответе API.
    /// Значение формируется сервером OK.ru при регистрации пользователя и не изменяется.
    /// Используется как ключевой параметр для всех операций с пользователем:
    /// <list type="bullet">
    /// <item>Получение расширенной информации (<c>users.getInfo</c>);</item>
    /// <item>Добавление в друзья (<c>friends.add</c>);</item>
    /// <item>Отправка сообщений или уведомлений;</item>
    /// <item>Проверка членства в группах;</item>
    /// <item>Получение списка фотографий пользователя.</item>
    /// </list>
    /// 
    /// <para><b>Формат идентификатора:</b></para>
    /// <list type="bullet">
    /// <item>Строковое представление числового значения (например, <c>"123456789"</c>);</item>
    /// <item>Уникален в пределах всей платформы Одноклассников;</item>
    /// <item>Не изменяется при смене имени, фамилии или других данных профиля.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/>.
    /// </value>
    [JsonPropertyName("uid")]
    public required string UID { get; init; }
}