namespace Oland.Odnoklassniki.Rest.ApiClients.Photos.Datas;

/// <summary>
/// DTO для представления информации об альбоме фотографий в Одноклассниках.
/// Используется для передачи данных альбомов при получении списков и управлении фотографиями.
/// </summary>
/// <remarks>
/// Класс соответствует структуре данных, возвращаемой методами API работы с альбомами
/// (например, <c>photos.getAlbums</c>, <c>photos.createAlbum</c>).
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Отображение списка альбомов пользователя или группы;</item>
/// <item>Проверка прав на добавление фотографий в альбом;</item>
/// <item>Идентификация альбома при загрузке новых изображений.</item>
/// </list>
/// </remarks>
public record AlbumData
{
    /// <summary>
    /// Заголовок (название) альбома. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>title</c> в ответе API Одноклассников.
    /// Может содержать произвольный текст, включая пробелы и специальные символы.
    /// Максимальная длина ограничена настройками платформы OK.ru (обычно до 255 символов).
    /// 
    /// <para><b>Примеры значений:</b></para>
    /// <list type="bullet">
    /// <item>«Фотографии со дня рождения»;</item>
    /// <item>«Профильные фото»;</item>
    /// <item>«Фотографии группы».</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение названия альбома. Не может быть пустым или <see langword="null"/>.
    /// </value>
    public required string Title { get; init; }

    /// <summary>
    /// Уникальный идентификатор альбома в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>id</c> или <c>aid</c> в ответе API.
    /// Значение формируется сервером OK.ru при создании альбома и не изменяется.
    /// Используется как ключевой параметр для всех операций с альбомом:
    /// <list type="bullet">
    /// <item>Загрузка фотографий (<c>photos.upload</c>);</item>
    /// <item>Получение списка фотографий (<c>photos.get</c>);</item>
    /// <item>Редактирование или удаление альбома.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/>.
    /// </value>
    public required string Id { get; init; }

    /// <summary>
    /// Идентификатор пользователя, которому принадлежит альбом.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>uid</c> или <c>owner_id</c> в ответе API.
    /// Заполняется для пользовательских альбомов. Для групповых альбомов может отсутствовать
    /// или содержать идентификатор группы (в зависимости от метода API).
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле необязательное — может быть <see langword="null"/> для системных альбомов;</item>
    /// <item>Используется для определения владельца и проверки прав доступа;</item>
    /// <item>При работе с групповыми альбомами рекомендуется дополнительно указывать <c>groupId</c>.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор пользователя в формате OK.ru.
    /// Может быть <see langword="null"/>, если альбом не привязан к конкретному пользователю.
    /// </value>
    public string? UserId { get; init; }
    
    /// <summary>
    /// Флаг, определяющий возможность добавления новых фотографий в альбом текущим пользователем.
    /// </summary>
    /// <remarks>
    /// Значение вычисляется на стороне сервера с учётом:
    /// <list type="bullet">
    /// <item>Роли пользователя (владелец, модератор, участник, посторонний);</item>
    /// <item>Настроек приватности альбома (публичный, только для друзей, закрытый);</item>
    /// <item>Статуса альбома (архивный, удалённый, полный).</item>
    /// </list>
    /// 
    /// <para><b>Значения флага:</b></para>
    /// <list type="bullet">
    /// <item><c>true</c> — операция добавления фотографии разрешена;</item>
    /// <item><c>false</c> — операция запрещена (недостаточно прав или альбом закрыт).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Булево значение права доступа. По умолчанию <c>false</c> при отсутствии явных прав.
    /// </value>
    public bool IsAddPhotoAllowed { get; init; }
}