using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.Photos.Response.Photo;

/// <summary>
/// Внутренняя модель ответа API Одноклассников при получении списка фотографий.
/// Используется для десериализации JSON-ответа от метода <c>photos.get</c> с поддержкой пагинации.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, коллекцию <see cref="Datas.PhotoData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "photos": [
///     { "id": "123", "text": "Фото 1", "pic_max": "https://...", "user_id": "456" },
///     { "id": "789", "text": "Фото 2", "pic_max": "https://...", "user_id": "456" }
///   ],
///   "anchor": "abc123xyz",
///   "hasMore": true,
///   "totalCount": 150
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Получение списка фотографий в альбоме или на странице пользователя с пагинацией;</item>
/// <item>Определение наличия следующих страниц через <see cref="HasMore"/>;</item>
/// <item>Использование <see cref="Anchor"/> для загрузки следующей порции данных;</item>
/// <item>Преобразование коллекции <see cref="Photos"/> в публичные DTO для клиентского кода.</item>
/// </list>
/// 
/// <para><b>Особенности пагинации:</b></para>
/// API Одноклассников использует cursor-based пагинацию (на основе анкоров).
/// Для загрузки следующей страницы необходимо передать значение <see cref="Anchor"/>
/// в параметре <c>anchor</c> следующего запроса к <c>photos.get</c>.
/// </remarks>
internal class PhotosResponse
{
    /// <summary>
    /// Коллекция фотографий, возвращённых в текущей странице ответа. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>photos</c> в ответе API Одноклассников.
    /// Содержит коллекцию объектов <see cref="PhotoModel"/> с информацией о фотографиях.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Коллекция может быть пустой, если фотографии отсутствуют или достигнут конец выборки;</item>
    /// <item>Количество фотографий в одной странице ограничено параметром <c>count</c> запроса;</item>
    /// <item>Порядок фотографий определяется параметром сортировки запроса (по умолчанию — по дате загрузки).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Коллекция объектов <see cref="PhotoModel"/>. Не может быть <see langword="null"/>,
    /// но может быть пустой при отсутствии фотографий.
    /// </value>
    [JsonPropertyName("photos")]
    public required ICollection<PhotoModel> Photos { get; init; }

    /// <summary>
    /// Уникальный анкор (курсор) для загрузки следующей страницы фотографий. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>anchor</c> в ответе API Одноклассников.
    /// Используется в cursor-based пагинации для перехода к следующей порции данных.
    /// 
    /// <para><b>Сценарий использования:</b></para>
    /// <list type="number">
    /// <item>Получить текущую страницу фотографий через <c>photos.get</c>;</item>
    /// <item>Проверить флаг <see cref="HasMore"/> — если <c>true</c>, продолжить загрузку;</item>
    /// <item>Передать значение <see cref="Anchor"/> в параметре <c>anchor</c> следующего запроса;</item>
    /// <item>Повторять до тех пор, пока <see cref="HasMore"/> не станет <c>false</c>.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Анкор действителен только для текущей сессии и конкретного запроса;</item>
    /// <item>Не рекомендуется кэшировать анкору между разными вызовами API;</item>
    /// <item>Пустой анкор (<c>""</c>) означает начало выборки (первая страница).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение курсора пагинации. Не может быть пустым или <see langword="null"/>
    /// для ответов с пагинацией, но может быть пустой строкой для первой страницы.
    /// </value>
    [JsonPropertyName("anchor")]
    public string? Anchor { get; init; }

    /// <summary>
    /// Флаг наличия дополнительных страниц с фотографиями.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>hasMore</c> в ответе API.
    /// Используется для определения необходимости загрузки следующей страницы данных.
    /// 
    /// <para><b>Значения флага:</b></para>
    /// <list type="bullet">
    /// <item><c>true</c> — существуют дополнительные фотографии, доступные для загрузки;</item>
    /// <item><c>false</c> — все фотографии возвращены, дальнейшая пагинация не требуется.</item>
    /// </list>
    /// 
    /// <para><b>Рекомендация:</b></para>
    /// При <c>true</c> следует использовать значение <see cref="Anchor"/> для запроса следующей страницы.
    /// </remarks>
    /// <value>
    /// Булево значение наличия следующих записей.
    /// </value>
    [JsonPropertyName("hasMore")]
    public bool HasMore { get; init; }

    /// <summary>
    /// Общее количество фотографий, доступных пользователю или в альбоме.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>totalCount</c> в ответе API.
    /// Содержит полное число фотографий без учёта текущей пагинации.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Значение может быть приблизительным для очень больших коллекций (>10000 фотографий);</item>
    /// <item>Поле может отсутствовать или возвращать <c>0</c>, если подсчёт отключён на стороне сервера;</item>
    /// <item>Используется для отображения прогресса загрузки и общего количества элементов.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Целочисленное значение общего количества фотографий.
    /// </value>
    [JsonPropertyName("totalCount")]
    public int TotalCount { get; init; }
}