using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.Photos.Response.Album;

/// <summary>
/// Внутренняя модель ответа API Одноклассников при получении списка альбомов фотографий.
/// Используется для десериализации JSON-ответа от метода <c>photos.getAlbums</c> с поддержкой пагинации.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, коллекцию <see cref="Datas.AlbumData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "albums": [
///     { "title": "Фото 1", "aid": "123", "user_id": "456" },
///     { "title": "Фото 2", "aid": "789", "user_id": "456" }
///   ],
///   "hasMore": true,
///   "totalCount": 50,
///   "pagingAnchor": "abc123xyz"
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Получение списка альбомов пользователя или группы с пагинацией;</item>
/// <item>Определение наличия следующих страниц через <see cref="HasMore"/>;</item>
/// <item>Использование <see cref="Anchor"/> для загрузки следующей порции данных;</item>
/// <item>Преобразование массива <see cref="Albums"/> в публичные DTO для клиентского кода.</item>
/// </list>
/// 
/// <para><b>Особенности пагинации:</b></para>
/// API Одноклассников использует cursor-based пагинацию (на основе анкоров).
/// Для загрузки следующей страницы необходимо передать значение <see cref="Anchor"/>
/// в параметре <c>anchor</c> следующего запроса к <c>photos.getAlbums</c>.
/// </remarks>
public record AlbumsResponse
{
    /// <summary>
    /// Массив альбомов фотографий, возвращённых в текущей странице ответа.
    /// Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>albums</c> в ответе API Одноклассников.
    /// Содержит коллекцию объектов <see cref="AlbumModel"/> с информацией об альбомах.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Массив может быть пустым, если альбомы отсутствуют или достигнут конец выборки;</item>
    /// <item>Количество альбомов в одной странице ограничено параметром <c>count</c> запроса;</item>
    /// <item>Порядок альбомов определяется параметром сортировки запроса (по умолчанию — по дате создания).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Массив объектов <see cref="AlbumModel"/>. Не может быть <see langword="null"/>,
    /// но может быть пустым при отсутствии альбомов.
    /// </value>
    [JsonPropertyName("albums")]
    public required AlbumModel[] Albums { get; init; }

    /// <summary>
    /// Флаг наличия дополнительных страниц с альбомами.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>hasMore</c> в ответе API.
    /// Используется для определения необходимости загрузки следующей страницы данных.
    /// 
    /// <para><b>Значения флага:</b></para>
    /// <list type="bullet">
    /// <item><c>true</c> — существуют дополнительные альбомы, доступные для загрузки;</item>
    /// <item><c>false</c> — все альбомы возвращены, дальнейшая пагинация не требуется.</item>
    /// </list>
    /// 
    /// <para><b>Рекомендация:</b></para>
    /// При <c>true</c> следует использовать значение <see cref="Anchor"/> для запроса следующей страницы.
    /// </remarks>
    /// <value>
    /// Булево значение наличия следующих записей.
    /// </value>
    [JsonPropertyName("hasMore")]
    public bool HasMore { get; init; }

    /// <summary>
    /// Общее количество альбомов, доступных пользователю или группе.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>totalCount</c> в ответе API.
    /// Содержит полное число альбомов без учёта текущей пагинации.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Значение может быть приблизительным для очень больших коллекций (>10000 альбомов);</item>
    /// <item>Поле может отсутствовать или возвращать <c>0</c>, если подсчёт отключён на стороне сервера;</item>
    /// <item>Используется для отображения прогресса загрузки и общего количества элементов.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Целочисленное значение общего количества альбомов.
    /// </value>
    [JsonPropertyName("totalCount")]
    public int TotalCount { get; init; }

    /// <summary>
    /// Уникальный анкор (курсор) для загрузки следующей страницы альбомов. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>pagingAnchor</c> в ответе API Одноклассников.
    /// Используется в cursor-based пагинации для перехода к следующей порции данных.
    /// 
    /// <para><b>Сценарий использования:</b></para>
    /// <list type="number">
    /// <item>Получить текущую страницу альбомов через <c>photos.getAlbums</c>;</item>
    /// <item>Проверить флаг <see cref="HasMore"/> — если <c>true</c>, продолжить загрузку;</item>
    /// <item>Передать значение <see cref="Anchor"/> в параметре <c>anchor</c> следующего запроса;</item>
    /// <item>Повторять до тех пор, пока <see cref="HasMore"/> не станет <c>false</c>.</item>
    /// </list>
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Анкор действителен только для текущей сессии и конкретного запроса;</item>
    /// <item>Не рекомендуется кэшировать анкору между разными вызовами API;</item>
    /// <item>Пустой анкор (<c>""</c>) означает начало выборки (первая страница).</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковое значение курсора пагинации. Не может быть пустым или <see langword="null"/>
    /// для ответов с пагинацией, но может быть пустой строкой для первой страницы.
    /// </value>
    [JsonPropertyName("pagingAnchor")]
    public required string Anchor { get; init; }
}