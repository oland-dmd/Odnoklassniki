using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.Photos.Response.Album;

/// <summary>
/// Внутренняя модель ответа API Одноклассников при получении информации об альбоме фотографий.
/// Используется для десериализации JSON-ответа от методов работы с альбомами
/// (например, <c>photos.getAlbums</c>, <c>photos.createAlbum</c>).
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="Datas.AlbumData"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "title": "Фотографии со дня рождения",
///   "aid": "123456",
///   "user_id": "789012",
///   "attrs": { "flags": "ap" }
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Получение списка альбомов пользователя или группы;</item>
/// <item>Проверка атрибутов альбома (флаги доступа, тип, статус);</item>
/// <item>Преобразование в публичную модель для передачи в клиентский код.</item>
/// </list>
/// </remarks>
public record AlbumModel
{
    /// <summary>
    /// Заголовок (название) альбома. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>title</c> в ответе API Одноклассников.
    /// Может содержать произвольный текст, включая пробелы и специальные символы.
    /// Максимальная длина ограничена настройками платформы OK.ru (обычно до 255 символов).
    /// </remarks>
    /// <value>
    /// Строковое значение названия альбома. Не может быть пустым или <see langword="null"/>.
    /// </value>
    [JsonPropertyName("title")]
    public required string Title { get; init; }

    /// <summary>
    /// Уникальный идентификатор альбома в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>aid</c> (album ID) в ответе API.
    /// Значение формируется сервером OK.ru при создании альбома и не изменяется.
    /// Используется как ключевой параметр для всех операций с альбомом:
    /// <list type="bullet">
    /// <item>Загрузка фотографий (<c>photos.upload</c>);</item>
    /// <item>Получение списка фотографий (<c>photos.get</c>);</item>
    /// <item>Редактирование или удаление альбома.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым или <see langword="null"/>.
    /// </value>
    [JsonPropertyName("aid")]
    public required string Id { get; init; }

    /// <summary>
    /// Идентификатор пользователя, которому принадлежит альбом.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>user_id</c> в ответе API.
    /// Заполняется для пользовательских альбомов. Для групповых альбомов может отсутствовать
    /// или содержать идентификатор группы (в зависимости от метода API).
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле необязательное — может быть <see langword="null"/> для системных альбомов;</item>
    /// <item>Используется для определения владельца и проверки прав доступа;</item>
    /// <item>При работе с групповыми альбомами рекомендуется дополнительно указывать <c>groupId</c>.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Строковый идентификатор пользователя в формате OK.ru.
    /// Может быть <see langword="null"/>, если альбом не привязан к конкретному пользователю.
    /// </value>
    [JsonPropertyName("user_id")]
    public string? UserId { get; init; }

    /// <summary>
    /// Дополнительные атрибуты альбома (флаги, метаданные, настройки).
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>attrs</c> в ответе API.
    /// Содержит расширенную информацию об альбоме: флаги доступа, тип альбома,
    /// количество фотографий и другие служебные данные.
    /// 
    /// <para><b>Важно:</b></para>
    /// <list type="bullet">
    /// <item>Поле может отсутствовать в ответе, если не запрошено явно в параметрах метода;</item>
    /// <item>Структура атрибутов определяется версией API и типом запроса;</item>
    /// <item>Десериализуется в модель <see cref="Attributes"/>.</item>
    /// </list>
    /// </remarks>
    /// <value>
    /// Объект <see cref="Attributes"/> с дополнительными данными альбома.
    /// Может быть <see langword="null"/>, если атрибуты не возвращены сервером.
    /// </value>
    [JsonPropertyName("attrs")]
    public Models.Attributes? Attributes { get; init; }
}