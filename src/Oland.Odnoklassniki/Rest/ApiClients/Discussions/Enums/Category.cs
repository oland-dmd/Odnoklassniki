namespace Oland.Odnoklassniki.Rest.ApiClients.Discussions.Enums;

/// <summary>
/// Определяет категорию обсуждений в социальной сети «Одноклассники».
/// Используется для фильтрации или маршрутизации запросов к API (например, при вызове методов
/// получения обсуждений или комментариев).
/// </summary>
internal enum Category
{
    /// <summary>
    /// Обсуждения, связанные с группами (сообществами) — например, комментарии к постам на стене группы,
    /// фотографиям в альбомах сообщества или медиатопикам, опубликованным от имени группы.
    /// В API Одноклассников такие обсуждения обычно содержат ненулевой <c>groupId</c>.
    /// </summary>
    GROUP,

    /// <summary>
    /// Обсуждения, принадлежащие текущему пользователю — например, комментарии к его личным альбомам,
    /// записям на стене профиля или загруженным медиафайлам.
    /// В API Одноклассников такие обсуждения не привязаны к группе (<c>groupId</c> отсутствует или равен ID пользователя).
    /// </summary>
    MY
}