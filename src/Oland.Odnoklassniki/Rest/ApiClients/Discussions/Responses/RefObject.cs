using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.Discussions.Responses;

/// <summary>
/// Представляет ссылочный объект, связанный с обсуждением в «Одноклассниках».
/// Используется для передачи дополнительного контекста о связанных сущностях —
/// например, о группе, пользователе или альбоме, к которым относится обсуждение.
/// Чаще всего содержится в массиве <c>ref_objects</c> в ответе методов вроде <c>discussions.get</c>.
/// </summary>
internal record RefObject
{
    /// <summary>
    /// Уникальный идентификатор ссылочного объекта в системе Одноклассников.
    /// Тип идентификатора зависит от значения поля <see cref="Type"/>:
    /// — для пользователя: ID профиля (например, "123456789");
    /// — для группы: ID сообщества (например, "-123456789" или просто "123456789", в зависимости от формата API);
    /// — для альбома или медиа: соответствующий ID объекта.
    /// Соответствует полю <c>"id"</c> в JSON-ответе OK API.
    /// </summary>
    [JsonPropertyName("id")]
    public string ID { get; init; }

    /// <summary>
    /// Тип ссылочного объекта. Определяет, к какой сущности относится <see cref="ID"/>.
    /// Возможные значения (в зависимости от контекста API OK):
    /// — <c>"USER"</c> — обычный пользователь;
    /// — <c>"GROUP"</c> — сообщество (группа);
    /// — <c>"ALBUM"</c> — фотоальбом;
    /// — <c>"PHOTO"</c>, <c>"VIDEO"</c>, <c>"MEDIATOPIC"</c> — медиаконтент.
    /// Это поле позволяет корректно интерпретировать <see cref="ID"/> и при необходимости
    /// выполнять дополнительные запросы к соответствующим методам API.
    /// Соответствует полю <c>"type"</c> в JSON-ответе.
    /// </summary>
    [JsonPropertyName("type")]
    public string Type { get; init; }
}