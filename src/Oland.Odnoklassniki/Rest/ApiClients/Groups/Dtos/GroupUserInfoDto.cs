namespace Oland.Odnoklassniki.Rest.ApiClients.Groups.Dtos;

/// <summary>
/// DTO для представления сведений об участнике группы в Одноклассниках.
/// Содержит идентификатор пользователя и его статус в контексте конкретной группы.
/// Используется в методах получения списка участников и проверки прав доступа.
/// </summary>
public record GroupUserInfoDto
{
    /// <summary>
    /// Уникальный идентификатор пользователя в системе Одноклассников. Обязательное поле.
    /// </summary>
    /// <remarks>
    /// Используется для связки с методами API пользователей (например, <c>users.getInfo</c>).
    /// Значение должно соответствовать формату ID, возвращаемому сервисом ОК.
    /// </remarks>
    public required string UserId { get; init; }

    /// <summary>
    /// Статус участия пользователя в группе (роль, состояние блокировки и т.д.).
    /// </summary>
    /// <value>
    /// Значение перечисления <see cref="GroupStatus"/>.
    /// </value>
    /// <remarks>
    /// Определяет права доступа пользователя к функциям группы (модерация, публикация контента).
    /// Значение вычисляется на стороне сервера при запросе списка участников.
    /// </remarks>
    public GroupStatus Status { get; init; }
}