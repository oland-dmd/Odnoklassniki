using System.Text.Json.Serialization;

namespace Oland.Odnoklassniki.Rest.ApiClients.Groups.Responses;

/// <summary>
/// Внутренняя модель ответа API Одноклассников для операций с участием в группах.
/// Используется для десериализации JSON-ответа при добавлении/удалении пользователя из группы.
/// </summary>
/// <remarks>
/// Класс является промежуточной моделью для преобразования ответа API в публичные DTO
/// (например, <see cref="Dtos.UserGroupDto"/>). Не предназначен для прямого использования в бизнес-логике.
/// 
/// <para><b>Пример ответа сервера:</b></para>
/// <code>
/// {
///   "groupId": "123456",
///   "userId": "789012"
/// }
/// </code>
/// 
/// <para><b>Сценарии использования:</b></para>
/// <list type="bullet">
/// <item>Подтверждение вступления пользователя в группу;</item>
/// <item>Подтверждение исключения пользователя из группы;</item>
/// <item>Валидация связи между пользователем и группой после операции.</item>
/// </list>
/// </remarks>
internal record GroupResponse
{
    /// <summary>
    /// Уникальный идентификатор группы в системе Одноклассников.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>groupId</c> в ответе API.
    /// Значение формируется сервером OK.ru и используется для идентификации группы
    /// во всех последующих операциях (получение информации, управление участниками и т.д.).
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым для успешного ответа.
    /// </value>
    [JsonPropertyName("groupId")]
    public string GroupId { get; init; }
    
    /// <summary>
    /// Уникальный идентификатор пользователя в системе Одноклассников.
    /// </summary>
    /// <remarks>
    /// Соответствует полю <c>userId</c> в ответе API.
    /// Значение идентифицирует пользователя, для которого была выполнена операция
    /// с группой (вступление, исключение, проверка членства).
    /// </remarks>
    /// <value>
    /// Строковый идентификатор в формате OK.ru (числовая строка).
    /// Не может быть пустым для успешного ответа.
    /// </value>
    [JsonPropertyName("userId")]
    public string UserId { get; init; }
}